public class DoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int cont = 0 ;
        do {   System.out.println ("Cuenta: " + cont );
                   cont ++;
        } while (cont<=10);     } 
    }
  
